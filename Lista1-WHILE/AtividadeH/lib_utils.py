def obter_num_float(label):
    numero = float(input(label))
    return numero

def obter_num_int(label):
    numero = int(input(label))
    return numero

def mostrar_texto(texto):
    saida = print(texto)
    return saida

def obter_texto(texto):
    entrada = input(texto)
    return entrada

def obter_media(num1,num2):
    return num1/num2

def obter_percentual(num1,num2):
    return num1/num2 * 100
     